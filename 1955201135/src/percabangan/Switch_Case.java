package percabangan;

/**
 *
 * @author Toshiba
 */
public class Switch_Case {
     public static void main(String[] args) {
        // Percabangan Switch Case
        int nomor = 4;
        switch (nomor)
        {
            case 1:
                System.out.println("sepatu");
                break;
            case 2:
                System.out.println("baju");
                break;
            case 3:
                System.out.println("topi");
                break;
            case 4:
                System.out.println("tas");
                break;
            case 5:
                System.out.println("uang");
                break;
        }
    }
    
}
