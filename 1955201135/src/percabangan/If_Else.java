public class If_Else {
     public static void main(String[] args) {
        // Percabangan If Else
        int angka = 10;
        if (angka%5==0){
            System.out.println("Angka adalah bilangan genap "+angka);
        }
        else {
            System.out.println("Angka adalah bilangan ganjil "+angka);
        }
    }
    
}
